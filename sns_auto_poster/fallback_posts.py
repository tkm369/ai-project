_FALLBACK_LOG_FILE = os.path.join(os.path.dirname(__file__), "fallback_used.json")


def _load_used() -> set:
    """使用済みテンプレートインデックスを読み込む"""
    if not os.path.exists(_FALLBACK_LOG_FILE):
        return set()
    try:
        with open(_FALLBACK_LOG_FILE, encoding="utf-8") as f:
            return set(json.load(f))
    except Exception:
        return set()


def _save_used(used: set):
    """使用済みテンプレートインデックスを保存"""
    try:
        with open(_FALLBACK_LOG_FILE, "w", encoding="utf-8") as f:
            json.dump(sorted(used), f)
    except Exception:
        pass


def get_fallback_post() -> str:
    """未使用のテンプレートからランダムに1件返す
    全件使用済みの場合はリセットして再利用する
    """
    used = _load_used()
    total = len(_TEMPLATES)

    # 全件使用済みならリセット
    if len(used) >= total:
        print("  [fallback] テンプレート全件使用済み → リセット")
        used = set()

    # 未使用インデックスからランダム選択
    unused = [i for i in range(total) if i not in used]
    idx = random.choice(unused)

    # 使用済みに追加
    used.add(idx)
    _save_used(used)

    text = _TEMPLATES[idx]
    print(f"  [fallback] テンプレート #{idx+1}/{total} を使用")
    return text
